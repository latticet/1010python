# 查看数据类型
# type(数据)

# 数字（整型， 浮点型）
num1 = 100
num2 = 1.1
print(type(num1))
print(type(num2))
print('==' * 20)

# 字符串
# 只要用单引号或者双引号包裹的就是字符串
str1 = 'hello'
str2 = "good"
print(type(str1))
print(type(str2))
print('==' * 20)

# 布尔类型
bool1 = True
bool2 = False
print(type(bool1))
print(type(bool2))
print('==' * 20)

